public class Animal {
    private String nome;
    private byte idade;
    private String especie;
    private String sexo;
    private double peso;

    public Animal(String nome, byte idade, String especie, String sexo, double peso) {
        this.nome = nome;
        this.idade = idade;
        this.especie = especie;
        this.sexo = sexo;
        this.peso = peso;
    }

}